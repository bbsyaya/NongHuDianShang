package com.farmerec.entity.goods;

public class Trolley {

	private String trolleyInfoId;
	private String user_id;
	private int quantity;
	/**
	 * @return the trolleyInfoId
	 */
	public String getTrolleyInfoId() {
		return trolleyInfoId;
	}
	/**
	 * @param trolleyInfoId the trolleyInfoId to set
	 */
	public void setTrolleyInfoId(String trolleyInfoId) {
		this.trolleyInfoId = trolleyInfoId;
	}
	/**
	 * @return the user_id
	 */
	public String getUser_id() {
		return user_id;
	}
	/**
	 * @param user_id the user_id to set
	 */
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	/**
	 * @return the quantity
	 */
	public int getQuantity() {
		return quantity;
	}
	/**
	 * @param quantity the quantity to set
	 */
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
}
